<p>The ImageEditor class allows you to easily edit images using PHP on the fly.   It includes basic image editing functions, such as resizing, cropping, adding   text and adding lines.<br />
    <br />
  The ImageEditor class utilizes the GD2 library so   you must have it installed on the web server which you are planning on using the   ImageEditor class on. <br />
